test('hook handles missing target gracefully — emits ancestor READMEs for prospective Write', () => {
  const root = freshGitRepo();
  try {
    file(root, 'README.md', '<!-- auto-generated by docgen -->\nROOT');
    file(root, 'pkg/README.md', '<!-- auto-generated by docgen -->\nPKG');
    const sessionId = 'sess-write-' + Date.now();
    clearDedup(sessionId);
    const r = runHook(
      {
        tool_name: 'Write',
        tool_input: { file_path: join(root, 'pkg/new-file.ts') }, // doesn't exist yet
      },
      { sessionId },
    );
    assert.equal(r.status, 0);
    assert.ok(r.stdout.includes('PKG'), 'parent dir README must be injected even for new files');
    clearDedup(sessionId);
  } finally {
    rmSync(root, { recursive: true, force: true });
  }
});

test('hook includes BOTH hand-written and generated READMEs', () => {
  const root = freshGitRepo();
  try {
    // Root README has no docgen marker → hand-written.
    file(root, 'README.md', '# My project\n\nHand-tuned.');
    // Child has the marker → generated.
    file(root, 'pkg/README.md', '<!-- auto-generated by docgen -->\n## Purpose\nGenerated.');
    file(root, 'pkg/code.ts', '// code');

    const sessionId = 'sess-mixed-' + Date.now();
    clearDedup(sessionId);
    const r = runHook(
      { tool_name: 'Read', tool_input: { file_path: join(root, 'pkg/code.ts') } },
      { sessionId },
    );
    assert.ok(r.stdout.includes('My project'), 'hand-written README must be included');
    assert.ok(r.stdout.includes('Generated.'), 'generated README must be included');
    assert.ok(r.stdout.includes('hand-written'), 'kind label hand-written must appear');
    assert.ok(r.stdout.includes('generated'), 'kind label generated must appear');
    clearDedup(sessionId);
  } finally {
    rmSync(root, { recursive: true, force: true });
  }
});

test('hook records the agents EXPLICIT README reads — skips them on later injection', () => {
  const root = freshGitRepo();
  try {
    file(root, 'README.md', '<!-- auto-generated by docgen -->\nROOT');
    file(root, 'pkg/README.md', '<!-- auto-generated by docgen -->\nPKG');
    file(root, 'pkg/sub/README.md', '<!-- auto-generated by docgen -->\nSUB');
    file(root, 'pkg/sub/file.ts', '// code');

    const sessionId = 'sess-explicit-' + Date.now();
    clearDedup(sessionId);

    // 1) Agent EXPLICITLY Reads pkg/README.md — hook NOOPs (no
    //    self-injection) but should record it as "already in context".
    const r1 = runHook(
      { tool_name: 'Read', tool_input: { file_path: join(root, 'pkg/README.md') } },
      { sessionId },
    );
    assert.equal(r1.stdout, '', 'reading a README itself must NOT emit injection');

    // 2) Agent Reads a sibling source file. Ancestor walk would include
    //    root, pkg, pkg/sub. But pkg/README.md was already Read, so it
    //    must be skipped while the others still appear.
    const r2 = runHook(
      { tool_name: 'Read', tool_input: { file_path: join(root, 'pkg/sub/file.ts') } },
      { sessionId },
    );
    assert.ok(r2.stdout.includes('ROOT'), 'root README must still appear');
    assert.ok(r2.stdout.includes('SUB'), 'sub README must still appear');
    assert.ok(!r2.stdout.includes('PKG'),
      `pkg README must NOT reappear — agent already read it. Got: ${r2.stdout.slice(0, 300)}`);

    clearDedup(sessionId);
  } finally {
    rmSync(root, { recursive: true, force: true });
  }
});

test('hook never throws on garbage stdin — silent exit 0', () => {
  const r = spawnSync('node', [HOOK], {
    input: 'not valid json {',
    encoding: 'utf8',
    env: { ...process.env, DOCGEN_HOOK_TRACE: '0' },
  });
  assert.equal(r.status, 0, 'hook must NEVER exit non-zero (would block tool call)');
  assert.equal(r.stdout, '');
});

test('hook never throws on empty stdin — silent exit 0', () => {
  const r = spawnSync('node', [HOOK], {
    input: '',
    encoding: 'utf8',
    env: { ...process.env, DOCGEN_HOOK_TRACE: '0' },
  });
  assert.equal(r.status, 0);
  assert.equal(r.stdout, '');
});
